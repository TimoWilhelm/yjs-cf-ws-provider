function pickRandom<T>(array: Array<T>): T {
	return array[Math.trunc(Math.random() * array.length)];
}

export function zooId(options?: { separator?: string; capitalize?: boolean }): string {
	const { separator = ' ', capitalize = false } = options || {};

	let adjective = pickRandom(adjectives);
	let animal = pickRandom(animals);

	if (capitalize) {
		adjective = capitalizeWord(adjective);
		animal = capitalizeWord(animal);
	}

	return `${adjective}${separator}${animal}`;
}

const capitalizeWord = (word: string) => word.charAt(0).toUpperCase() + word.slice(1);

